    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jPasswordFieldSenha.getText().equals(jPasswordFieldSenha2.getText())){
            mod.setUsuario(jTextFieldUsu.getText());
            mod.setSenha(jPasswordFieldSenha.getText());
            mod.setTipo((String) jComboBox1.getSelectedItem());
            control.Salvar(mod);  
            
        }else{
            JOptionPane.showMessageDialog(null, "As senhas n√£o correspondem");
        }
       desabilitaTexto();
       desabilitarBotoes();
       limparTexto();
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

