    public void habilitarTexto(){
        jTextFieldUsu.setEnabled(true);
        jPasswordFieldSenha.setEnabled(true);
        jPasswordFieldSenha2.setEnabled(true);
        jComboBox1.setEnabled(true); 
    }
    public void desabilitaTexto(){
        jTextFieldUsu.setEnabled(false);
        jPasswordFieldSenha.setEnabled(false);
        jPasswordFieldSenha2.setEnabled(false);
        jComboBox1.setEnabled(false);
    }
    public void habilitarBotoes(){
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(true);
        jButtonNovo.setEnabled(false);
    }
    public void desabilitarBotoes(){
        jButtonCancelar.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);
    }
    public void limparTexto(){
        jTextFieldUsu.setText("");
        jPasswordFieldSenha.setText("");
        jPasswordFieldSenha2.setText("");
    }
    
    
